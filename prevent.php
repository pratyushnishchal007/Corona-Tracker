<h3 class="colors ml-3 text-dark">How to Protect Yourself & Others</h3>

<div id="accordion">
  <div class="card">
    <div class="card-header" id="headingOne">
      <h5 class="mb-0">
        <button class="btn btn-link" data-toggle="collapse" data-target="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
        Wear a mask
        </button>
      </h5>
    </div>

    <div id="collapseOne" class="collapse show" aria-labelledby="headingOne" data-parent="#accordion">
      <div class="card-body">
        <ul>
          <li>If you are not fully vaccinated and aged 2 or older, you should wear a mask in indoor public places.</li>
          <li>In general, you do not need to wear a mask in outdoor settings.
          <ul>
              <li>In areas with <a href="https://covid.cdc.gov/covid-data-tracker/#county-view" target="_blank">high numbers of COVID-19 cases,</a> consider wearing a mask in crowded outdoor settings and for activities with close contact with others who are not fully vaccinated.</li>
          </ul>
          </li>
          <li>If you are fully vaccinated and have a condition or are taking medications that weaken your immune system, you may need to keep taking steps to protect yourself, like wearing a mask. Talk to your healthcare provider about steps you can take to manage your health and risks.</li>
          <li>If you are fully vaccinated, see <a href="https://www.cdc.gov/coronavirus/2019-ncov/vaccines/fully-vaccinated.html" target="_blank">When You’ve Been Fully Vaccinated.</a></li>
        </ul>
        Masks are required on planes, buses, trains, and other forms of public transportation traveling into, within, or out of the United States and in U.S. transportation hubs such as airports and stations. Travelers are not required to wear a mask in outdoor areas of a conveyance (like on a ferry or the top deck of a bus). CDC recommends that travelers who are not fully vaccinated continue to wear a mask and maintain physical distance when traveling.
      </div>
    </div>
  </div>
  
  <div class="card">
    <div class="card-header" id="headingTwo">
      <h5 class="mb-0">
        <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
        Stay 6 feet away from others
        </button>
      </h5>
    </div>
    <div id="collapseTwo" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
      <div class="card-body">
        <ul>
        <li><b>Inside your home:</b> Avoid close contact with people who are sick.
        <ul>
        <li>If possible, maintain 6 feet between the person who is sick and other household members.</li>
        </ul>
        </li>
        <li><b>Outside your home:</b> Put 6 feet of distance between yourself and people who don’t live in your household.
        <ul>
        <li>Remember that some people without symptoms may be able to spread virus.</li>
        <li><a href="https://www.cdc.gov/coronavirus/2019-ncov/prevent-getting-sick/social-distancing.html" target="_blank"></a></li>
        <li>Keeping distance from others is especially important for <a href="https://www.cdc.gov/coronavirus/2019-ncov/need-extra-precautions/people-at-increased-risk.html" target="_blank" rel="noopener noreferrer">people who are at higher risk of getting very sick.</a></li>
        </ul>
        </li>
        
        </ul>
      </div>
      </div>
    </div>
  
  <div class="card">
    <div class="card-header" id="headingThree">
      <h5 class="mb-0">
        <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
        Get Vaccinated
        </button>
      </h5>
    </div>
    <div id="collapseThree" class="collapse" aria-labelledby="headingThree" data-parent="#accordion">
      <div class="card-body">
        <ul>
          <li>Authorized COVID-19 vaccines can help protect you from COVID-19.</li>
        <li>You should get a <a href="https://www.cdc.gov/coronavirus/2019-ncov/vaccines/index.html" target="_blank">COVID-19 vaccine</a> when it is available to you.</li>
        <li><a href="https://www.cdc.gov/coronavirus/2019-ncov/vaccines/fully-vaccinated.html" target="_blank">Once you are fully vaccinated,</a> you may be able to start doing some things that you had stopped doing because of the pandemic.</li>
        </ul>
      </div>
    </div>
  </div>
  
  <div class="card">
    <div class="card-header" id="headingFour">
      <h5 class="mb-0">
        <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseFour" aria-expanded="false" aria-controls="collapseFour">
        Avoid crowds and poorly ventilated spaces
        </button>
      </h5>
    </div>
    <div id="collapseFour" class="collapse" aria-labelledby="headingFour" data-parent="#accordion">
      <div class="card-body">
        <ul>
        <li>Being in crowds like in restaurants, bars, fitness centers, or movie theaters puts you at higher risk for COVID-19.</li>
        <li>Avoid indoor spaces that do not offer fresh air from the outdoors as much as possible.</li>
        <li>If indoors, bring in fresh air by opening windows and doors, if possible.</li>
        </ul>
      </div>
    </div>
  </div>
  
  <div class="card">
    <div class="card-header" id="headingFive">
      <h5 class="mb-0">
        <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseFive" aria-expanded="false" aria-controls="collapseFive">
        Wash your hands often
        </button>
      </h5>
    </div>
    <div id="collapseFive" class="collapse" aria-labelledby="headingFive" data-parent="#accordion">
      <div class="card-body">
        <ul>
        <li><a href="https://www.cdc.gov/handwashing/when-how-handwashing.html" target="_blank">Wash your hands</a> often with soap and water for at least 20 seconds especially after you have been in a public place, or after blowing your nose, coughing, or sneezing.</li>
        <li>It’s especially important to wash:
        <ul>
        <li>Before eating or preparing food</li>
        <li>Before touching your face</li>
        <li>After using the restroom</li>
        <li>After leaving a public place</li>
        <li>After blowing your nose, coughing, or sneezing</li>
        <li>After handling your mask</li>
        <li>After changing a diaper</li>
        <li>After caring for someone sick</li>
        <li>After touching animals or pets</li>
        
        </ul>
        </li>
        <li>If soap and water are not readily available, <b>use a hand sanitizer that contains at least 60% alcohol.</b> Cover all surfaces of your hands and rub them together until they feel dry.</li>
        <li><b>Avoid touching your eyes, nose, and mouth</b> with unwashed hands.</li>
        </ul>
      </div>
    </div>
  </div>


  <div class="card">
    <div class="card-header" id="headingSix">
      <h5 class="mb-0">
        <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseSix" aria-expanded="false" aria-controls="collapseSix">
        Cover coughs and sneezes
        </button>
      </h5>
    </div>
    <div id="collapseSix" class="collapse" aria-labelledby="headingSix" data-parent="#accordion">
      <div class="card-body">
        <ul>
        <li><b>If you are wearing a mask:</b> You can cough or sneeze into your mask. Put on a new, clean mask as soon as possible and wash your hands.
</li>
        <li><b>If you are not wearing a mask:</b>
        <ul>
        <li>Always cover your mouth and nose with a tissue when you cough or sneeze, or use the inside of your elbow and do not spit.</li>
        <li>Throw used tissues in the trash.</li>
        
        </ul>
        </li>

        <li>Immediately <b>wash your hands</b> with soap and water for at least 20 seconds. If soap and water are not readily available, clean your hands with a hand sanitizer that contains at least 60% alcohol.</li>
        </ul>
      </div>
    </div>
  </div>


  <div class="card">
    <div class="card-header" id="headingSeven">
      <h5 class="mb-0">
        <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseSeven" aria-expanded="false" aria-controls="collapseSeven">
        Clean and disinfect
        </button>
      </h5>
    </div>
    <div id="collapseSeven" class="collapse" aria-labelledby="headingSeven" data-parent="#accordion">
      <div class="card-body">
        <ul>
        <li>Clean high touch surfaces daily. This includes tables, doorknobs, light switches, countertops, handles, desks, phones, keyboards, toilets, faucets, and sinks.</li>
        <li><b>If someone is sick or has tested positive for COVID-19, disinfect</b> <a href="https://www.cdc.gov/coronavirus/2019-ncov/prevent-getting-sick/disinfecting-your-home.html" target="_blank" rel="noopener noreferrer">frequently touched surfaces.</a> Use a household disinfectant product from <a href="https://www.epa.gov/coronavirus/about-list-n-disinfectants-coronavirus-covid-19-0" target="_blank" rel="noopener noreferrer"></a>EPA’s List N: Disinfectants for Coronavirus (COVID-19)external icon according to manufacturer’s labeled directions.
        
        <ul>
        <li><b>If surfaces are dirty, clean them</b>using detergent or soap and water prior to disinfection.</li>
        </ul>
        
        </li>
        
        </ul>
      </div>
    </div>
  </div>


  <div class="card">
    <div class="card-header" id="headingEight">
      <h5 class="mb-0">
        <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseEight" aria-expanded="false" aria-controls="collapseSeven">
        Monitor your health daily
        </button>
      </h5>
    </div>
    <div id="collapseEight" class="collapse" aria-labelledby="headingEight" data-parent="#accordion">
      <div class="card-body">
      <ul>
      <li><b>Be alert for symptoms.</b>Watch for fever, cough, shortness of breath, or <a href="https://www.cdc.gov/coronavirus/2019-ncov/symptoms-testing/symptoms.html" target="_blank">other symptoms </a>of COVID-19.
      <ul>
      <li>Especially important if you are <a href="https://www.cdc.gov/coronavirus/2019-ncov/daily-life-coping/essential-goods-services.html" target="_blank"> running essential errands,</a> going into the office or workplace, and in settings where it may be difficult to keep a <a href="https://www.cdc.gov/coronavirus/2019-ncov/prevent-getting-sick/social-distancing.html" target="_blank">physical distance of 6 feet.</a></li>
      
      </ul>
      
      </li>
      <li>Take your temperature if symptoms develop.
      
      <ul>
      <li>Don’t take your temperature within 30 minutes of exercising or after taking medications that could lower your temperature, like acetaminophen.
</li>
      </ul>
      </li>
      <li>Follow <a href="https://www.cdc.gov/coronavirus/2019-ncov/if-you-are-sick/steps-when-sick.html" target="_blank"></a>CDC guidance if symptoms develop.</li>
      
      </ul>
        
